public class Bait extends Object{
    private int price;
    private int effect;
    public Bait(int price, int effect) {
        this.price = price;
        this.effect = effect;
    }
    public int getPrice() {
        return price;
    }
    public void setPrice(int price) {
        this.price = price;
    }
    public int getEffect() {
        return effect;
    }
    public void setEffect(int effect) {
        this.effect = effect;
    }
    
}
